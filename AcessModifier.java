//Access Modifiers public,private,protector and default.
//Access Modifiers are keywords that determine the visibility and access level of a class,
//method, or data member. public: A public class, method, or data member is visible to all classes. 
//protected: A protected class, method, or data member can be accessed by classes within the same
//package, and any subclasses which extend the class. default: A default class, method, or data member is visible only to classes
//within the same package.private: A private class, method, or data member is only visible to the class it is declared in, 
//and not to any subclasses.

public class AcessModifier {
    public static void main(String arg[]){
        int n=13;
        System.out.println(n);
    };
};
